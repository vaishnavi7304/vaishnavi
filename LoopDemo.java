public class LoopDemo 
{
    public static void main(String args[]) 
    { 
    
        for(int i=0;i<=10;i++)
        {
            if(i%2==0)
            {
                System.out.println("This is even no.:"+i);
            }
            else
            {
                System.out.println("This is odd no.:"+i);
            }
        }
        
    }
    
}
